# Indentated block of code begins with a colon (:). 
# Correct indentation is crucial in Python. It makes code readable and defines the structure of the code.
# Indentation is required for conditional statements, loops (if, else, for loop, while loop), functions, and classes. Or an error will occur.
# Correct indentation example:
if 5 > 2:
    print("Five is greater than two!")

# Incorrect indentation example:
# if 5 > 2:
# print("Five is greater than two!")