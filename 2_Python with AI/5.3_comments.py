# A comment looks like this in Python.
# Comments are used to explain code and make it more understandable.
# They are ignored by the Python interpreter when the code is executed.
# Single-line comments start with a hash symbol (#).
# Multi-line comments can be created using triple quotes (''' or """).
'''
This is a multi-line comment.
It can span multiple lines.
Useful for longer explanations or documentation.
'''
"""This is another multi-line comment.
Also useful for documentation."""
# Comments are essential for code maintenance and collaboration with other developers.
# Always strive to write clear and concise comments that add value to the code.
# Example of single-line comment:
print("Hello, World!")  # This prints a greeting message to the console.
# Example of multi-line comment:
'''
This function adds two numbers.
def add(a, b):
    return a + b
'''
def add(a, b):
    return a + b            # The above function adds two numbers and returns the result.
result = add(5, 3)        # Calling the add function with 5 and 3
print(result)             # This prints the result of the addition (8)
# Always remember to use comments wisely to enhance code readability!
# End of comments example.
# # Happy coding!
