# Identifier is a name used to identify a variable, function, class, module, or other object in Python.


# Identifier naming rules:
# 1. Must start with a letter (A-Z, a-z)
# 2. Followed by letters, digits (0-9), or underscores.
# 2. Case-sensitive (e.g., myVariable and myvariable are different).
# 3. Can be of any length.
# 4. Cannot be a reserved keyword (like if, else, while, for, global, class,etc.)
# 5. Cannot start with a digit (0-9).
# 6. Cannot contain spaces or special characters (like @, #, $, %, etc.)

