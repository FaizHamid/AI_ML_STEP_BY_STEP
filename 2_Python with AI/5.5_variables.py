# Variables in Python are used to store data values.
# In Python, you don't need to declare the type of a variable.
# The type is inferred from the value assigned to the variable.

# Example of variables:
x = 5           # An integer variable
y = "Hello"     # A string variable
price = 19.99    # A float variable
is_active = True  # A boolean variable
print(x)
print(y)
print(price)
print(is_active)


# Assigning multiple variables in a single line.
a, b, c = 1, 2.5, "World"
print(a)
print(b)
print(c)    